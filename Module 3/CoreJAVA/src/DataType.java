public class DataType {
    public static void main(String[] args) {
        int a = 10;
        char c = 'a';
        float f = 10.0F;
        double d = 10.00;
        boolean b = false;
        System.out.println("int : "+a + ", float :"+f +", double :"+d+ ", char :"+c +", boolean :"+b);

    }
}
